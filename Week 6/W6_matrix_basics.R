#This is declaration of matrixes

firstM <-matrix(c(1,2,3,4,5,6,7,8,9),nrow=3,ncol=3)

secondM <-matrix(c(1,0,0,0,1,0,0,0,1),nrow=3,ncol=3)

print("This is first matrix")
print(firstM)
print("This is second matrix/ identity matrix")
print(secondM)
#these are the basic operations between two matrixes
print("The addition between the two matrixes")
print(firstM+secondM)
print("The subtraction between the two matrixes")
print(firstM-secondM)
print("The multiplication between the two matrixes using'*'")
print(firstM*secondM)
print("The division between the two matrixes using '/'")
print(firstM/secondM)
